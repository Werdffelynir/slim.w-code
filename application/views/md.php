
    <h1>Специальные символы HTML</h1>

    <p><dfn>Специальные символы</dfn> в&nbsp;HTML реализуются с&nbsp;помощью интерпретируемых браузером мнемоник и&nbsp;числовых
        кодов. Например, двойную кавычку&nbsp;(") можно вывести кодом <code>&amp;quot;</code>, а&nbsp;знак евро&nbsp;(€)&nbsp;—
        <code>&amp;euro;</code>. Все мнемокоды начинаются с&nbsp;амперсанда&nbsp;(&amp;) и&nbsp;заканчиваются точкой с&nbsp;запятой&nbsp;(;).
        Чтобы обычный амперсанд в&nbsp;тексте не&nbsp;путался с&nbsp;началом мнемокода, его выводят с&nbsp;помощью кода
        <code>&amp;amp;</code>.</p>

    <p>Числовыми кодами можно отобразить любые символы UTF-8. Такие коды имеют вид <code>&amp;#xxxx;</code>, где вместо
        <code>xxxx</code> указывается десятичный номер символа в&nbsp;кодировке UTF-8. Например, <code>&amp;#169;</code>
        отобразится как «©».</p>


